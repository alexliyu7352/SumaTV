package com.ddm.live.models.bean.address;

public class Location {

    private Double lng;
    private Double lat;


    /**
     * @return The lng
     */
    public Double getLng() {
        return lng;
    }

    /**
     * @param lng The lng
     */
    public void setLng(Double lng) {
        this.lng = lng;
    }

    /**
     * @return The lat
     */
    public Double getLat() {
        return lat;
    }


}